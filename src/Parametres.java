/**
 * Classe Parametres utilisée pour stocker les paramètres globaux de l'application.
 * Elle permet de configurer certains aspects du fonctionnement, comme l'activation ou la désactivation de la musique.
 */
public class Parametres {
    /**
     * Indique si la musique est activée ou non.
     * <p>
     * - `true` : La musique est activée.
     * - `false` : La musique est désactivée.
     * </p>
     * Par défaut, la musique est activée.
     */
    public static boolean musique = true;
    public static boolean effetsSonores = true;
}

